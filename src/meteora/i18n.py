_STORE = {"zh": _ZH, "en": _EN, "ja": _JA, "ko": _KO, "fr": _FR, "de": _DE, "es": _ES, "ru": _RU}

_SUPPORTED = {c: n for c, n in [
    ("zh", "中文"), ("en", "English"), ("ja", "日本語"),
    ("ko", "한국어"), ("fr", "Français"), ("de", "Deutsch"),
    ("es", "Español"), ("ru", "Русский"),
]}


def t(key: str, language: str, **kwargs) -> str:
    """Look up translation for key in given language."""
    table = _STORE.get(language, _EN)  # fallback to English for unknown lang
    val = table.get(key)
    if val is None:
        val = _EN.get(key)
    if val is None:
        return key
    if kwargs:
        val = val.format(**kwargs)
    return val


def language_label(code: str) -> str:
    """Return the display name of a language in that language itself."""
    return dict(_SUPPORTED).get(code, code)


def language_options() -> list[tuple[str, str]]:
    """Return (value, display_label) pairs for the language selector."""
    return list(_SUPPORTED.items())


def is_supported_language(code: str) -> bool:
    return code in dict(_SUPPORTED)
