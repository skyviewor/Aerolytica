"""Metadata for built-in datasets handled by dedicated download tools."""

from aero.datasets.models import DatasetSpec, DatasetVariable

ATMOSPHERIC_VARIABLES = (
    DatasetVariable(
        name="atmospheric_fields",
        long_name="atmospheric and surface meteorological fields",
        units="various",
        aliases=("温度", "风", "降水", "气压", "湿度", "temperature", "wind", "precipitation"),
    ),
)


def _spec(
    dataset_id: str,
    name: str,
    provider_id: str,
    provider_name: str,
    domain: str,
    description: str,
    spatial_resolution: str,
    temporal_resolution: str,
    download_tool: str,
    *,
    requires_auth: bool = False,
    download_granularity: str = "request dependent",
    aliases: tuple[str, ...] = (),
) -> DatasetSpec:
    variables = ATMOSPHERIC_VARIABLES
    if aliases:
        variables = (
            DatasetVariable(
                name="atmospheric_fields",
                long_name="atmospheric and surface meteorological fields",
                units="various",
                aliases=ATMOSPHERIC_VARIABLES[0].aliases + aliases,
            ),
        )
    return DatasetSpec(
        dataset_id=dataset_id,
        name=name,
        provider_id=provider_id,
        provider_name=provider_name,
        domain=domain,
        description=description,
        variables=variables,
        spatial_coverage="全球",
        temporal_coverage="由数据源实时确认",
        spatial_resolution=spatial_resolution,
        temporal_resolution=temporal_resolution,
        file_formats=("NetCDF", "GRIB2"),
        download_granularity=download_granularity,
        source_url="",
        citation_url="",
        download_tool=download_tool,
        requires_auth=requires_auth,
        supports_server_time_subset=True,
        supports_server_area_subset=provider_id == "ecmwf-cds",
        supports_resume=True,
    )


